#..............lists.............
l=[1,2,3,4,5]
print("This is a list")
print(l)

print("6 Appends in following list ")
l.append(6)
print(l)

print("7 is inserted in following list ")
l.insert(3,7)
print(l)

print("6,7  removed in following list ")
l.pop(6)
l.pop(3)
print(l)

print("Reversed list ")
l.reverse()
print(l)

print("sorting of given list ")
l.sort()
print(l)

print("length of given list ")
print(len(l))

print("max of given list ")
print(max(l))

print("min of given list ")
print(min(l))


